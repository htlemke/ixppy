def dSpace(ID,hkl):
    """ Computes the d spacing (m) of the specified material and reflection 
        ID is chemical fomula : 'Si'
        hkl is the reflection : (1,1,1)
    """
    ID=checkID(ID)
    h=hkl[0]
    k=hkl[1]
    l=hkl[2]

    lp=latticeParameters[ID]
    a=lp[0]/u['ang']
    b=lp[1]/u['ang']
    c=lp[2]/u['ang']
    alpha=lp[3]
    beta=lp[4]
    gamma=lp[5]

    ca=np.cos(np.pi/180*alpha)
    cb=np.cos(np.pi/180*beta)
    cg=np.cos(np.pi/180*gamma)
    sa=np.sin(np.pi/180*alpha)
    sb=np.sin(np.pi/180*beta)
    sg=np.sin(np.pi/180*gamma)

    invdsqr=1/(1+2*ca*cb*cg-ca**2-cb**2-cg**2)*(h**2*sa**2/a**2 + k**2*sb**2/b**2 + l**2*sg**2/c**2 +
      2*h*k*(ca*cb-cg)/a/b+2*k*l*(cb*cg-ca)/b/c+2*h*l*(ca*cg-cb)/a/c)
      
    d=invdsqr**-0.5
    return d



